# Everything in Ruby is Thruthie, except "false" and "nil"!
#
if 5 < 8 # true, code executed
  puts "That is true"
end

if true # code will be always executed
  puts "That is true again!"
end

if 5 # thruthie value
  puts "That is true once again!"
end

if "Hello"
  puts "That is true value as well!"
end

# The only falsie values in Ruby is "false" and "nil"!
#

if false # code not executed
  puts "This is false!"
end

if nil # code not executed
  puts "This is false again!"
end